vbsme:  
    add $v0, $zero, $zero             # reset $v0 and $V1
    add $v1, $zero, $zero
	addi $s1, $zero, 32767
	#permanent registers
	add $s4, $zero, $zero				# Set currRow to 0
	add $s5, $zero, $zero				# Set currCol to 0
