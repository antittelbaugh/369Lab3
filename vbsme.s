  addi $t2, $t2, 1           #increment exit check
  addi $t0, $zero, 1           #set temp to 1
  bne $s7, $t0, movesLeft     #if (pos[2]==1)
  sub $t5, $t8, $s6          #t5 = rowDiff-count
  slt $t0, $s4, $t5          #t4 = (currRow<rowDiff-count)
